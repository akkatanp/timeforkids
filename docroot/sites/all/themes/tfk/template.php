function tfk_header_tag($type) {
  $output = '';
  switch ($type) {
    case 'a_paper':
      $output = t('A+ Papers');
      break;
    case 'flashcard':
      $output = t('Flash-card Maker');
      break;
    case 'grammar_practice':
      $output = t('Punctuation Practice');
      break;
    case '':
      $output = drupal_get_title();
  }
  return $output;
}



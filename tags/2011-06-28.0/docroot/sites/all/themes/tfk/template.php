/**
 * For homework helper pages, create a section title based on content type.
 */
function tfk_content_type_title($type) {
  $output = '';
  switch ($type) {
    case 'a_paper':
      $output = t('A+ Papers');
      break;
    case 'flashcard':
      $output = t('Flashcard Maker');
  }
  return $output;
}


function tfk_css_alter(&$css) {
  //alter css to display as link tags
  foreach ($css as $key => $value) {
    $css[$key]['preprocess'] = FALSE;
  }
}

function tfk_preprocess_flashcard_cycle_mark(&$variables) {
  $variables['title'] = t('Mark this card for extra practice');
}
